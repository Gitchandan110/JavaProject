package packageJava;


public class JC_05_WhileLoop {

	public static void main(String[] args) {

		int a = 0;

		while (a < 10) {

			System.out.println(a);
	
			a++;

		}

		int b = 100;
		System.out.println(b);
		while (b >= 90) {

			System.out.println(b);
			b--;
		}

	}
}